# 并行、并发、同步、异步、进程和线程：概念关系与 Python 应用
#
# 一、先区分两个维度
# 1. 并发（concurrency）和并行（parallelism）描述“任务如何推进”：
#    - 并发：多个任务在同一段时间内都在推进。单核 CPU 也能通过任务切换实现，
#      例如下载文件时，程序在等待网络数据的间隙处理其他下载任务。
#    - 并行：多个任务在同一时刻真正同时执行，需要多个 CPU 核心或多个处理器。
#      并行一定是并发的一种，但并发不一定是并行。
#    - 一句话：并发关注“同时处理多件事”，并行关注“同时做多件事”。
#
# 2. 同步（synchronous）和异步（asynchronous）描述“任务如何等待结果”：
#    - 同步：发起操作后，当前执行流程要等操作完成并取得结果，才继续下一步。
#      例如普通函数调用、直接读取一个文件；代码顺序直观，但等待期间可能闲置。
#    - 异步：发起操作后不必原地等待；操作完成时再通过 await、回调或通知取得结果。
#      异步尤其适合网络、磁盘、数据库等 I/O 等待很多的场景。
#    - 同步/异步不等于串行/并行：异步程序可只运行在一个线程；多线程代码也可因
#      lock、join 等等待操作而表现为同步。
#
# 二、进程与线程是实现并发/并行的两类执行单元
# 1. 进程（process）
#    - 操作系统分配资源的基本单位；每个进程通常有独立的地址空间和 Python 解释器。
#    - 进程之间默认不共享普通变量，需要 Queue、Pipe、共享内存、文件或网络通信。
#    - 隔离性强，一个子进程异常通常不直接破坏其他进程；创建和通信成本相对较高。
#    - Python 中常用 multiprocessing.Process 或 concurrent.futures.ProcessPoolExecutor。
#
# 2. 线程（thread）
#    - CPU 调度的较小执行单位；同一进程的线程共享内存、文件描述符等资源。
#    - 共享数据很方便，但同时读写同一份数据会出现竞态条件，需要 Lock、Queue 等同步。
#    - 创建和切换成本通常低于进程，适合大量等待 I/O 的工作。
#    - Python 中常用 threading.Thread 或 concurrent.futures.ThreadPoolExecutor。
#
# 三、它们之间的典型组合
# - 单进程 + 单线程 + 同步：最简单，适合少量、顺序明确的任务。
# - 单进程 + 多线程：实现并发；很适合爬虫、HTTP 请求、文件读写等 I/O 密集型任务。
# - 多进程：可让多个 CPU 核心同时执行 Python 字节码，实现 CPU 密集型任务的并行。
# - 单线程 + asyncio：协程在 await 处主动让出执行权，用很少的线程处理很多 I/O 任务。
#   它是并发模型，不会让纯 Python 计算自动并行。
# - 多进程 + 每个进程多线程/协程：常见于服务端，用进程利用多核，用线程或协程处理 I/O。
#
# 四、Python 中一个关键限制：GIL（全局解释器锁）
# - 在主流 CPython 实现中，同一进程内通常只有一个线程能执行 Python 字节码。
# - 因而多线程不能有效加速纯 Python 的 CPU 密集型计算（如大量循环、复杂数值计算）。
# - 线程在 I/O 等待时会释放执行机会，所以对 I/O 密集型任务仍然非常实用。
# - CPU 密集型任务优先考虑 multiprocessing / ProcessPoolExecutor；若使用 NumPy 等
#   会在底层释放 GIL 的扩展库，线程也可能带来并行加速。
#
# 五、实用选型
# - 大量网络请求、数据库访问、磁盘读写：优先 asyncio（异步库齐全时）或线程池。
# - 图片处理、压缩、纯 Python 大计算：优先进程池，任务要足够大以覆盖进程通信开销。
# - 需要任务隔离或避免单个任务拖垮主程序：使用独立进程。
# - 共享状态多且逻辑复杂：优先 Queue 传递消息，少共享可变数据，避免过度加锁。
# - 并发数很高的网络服务：使用 asyncio，并保证所有 I/O 调用都是可 await 的异步 API；
#   在协程内调用阻塞函数会阻塞整个事件循环，应改用异步库或 asyncio.to_thread。
#
# 六、常用标准库对应关系
# - threading：手动创建和管理线程。
# - multiprocessing：手动创建进程、进程间通信和共享数据。
# - concurrent.futures：以 ThreadPoolExecutor / ProcessPoolExecutor 统一提交任务。
# - asyncio：事件循环、协程、await、异步任务管理。
# - queue：线程安全队列；multiprocessing.Queue 则用于进程间传递数据。
